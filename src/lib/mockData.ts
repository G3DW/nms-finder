export function computeConfidence(feedback: FeedbackRecord[]): ConfidenceRecord[] {
  const grouped = new Map<string, FeedbackRecord[]>();

  feedback.forEach((entry) => {
    const records = grouped.get(entry.location_id) ?? [];
    records.push(entry);
    grouped.set(entry.location_id, records);
  });

  return Array.from(grouped.entries()).map(([locationId, records]) => {
    const confirmedCount = records.filter((entry) => entry.feedback_type === 'still_here').length;
    const goneCount = records.filter((entry) => entry.feedback_type === 'gone').length;
    const totalVotes = records.length;

    return {
      location_id: locationId,
      confirmed_count: confirmedCount,
      gone_count: goneCount,
      total_votes: totalVotes,
      confidence_pct: totalVotes === 0 ? null : Math.round((confirmedCount / totalVotes) * 100),
    };
  });
}

export function buildMockLocationRecords(locations: Location[], feedback: FeedbackRecord[]): LocationRecord[] {
  const confidenceMap = new Map(computeConfidence(feedback).map((entry) => [entry.location_id, entry]));

  return locations.map((location) => ({
    ...location,
    location_confidence: confidenceMap.get(location.id) ?? {
      location_id: location.id,
      confirmed_count: 0,
      gone_count: 0,
      total_votes: 0,
      confidence_pct: null,
    },
  }));
}
